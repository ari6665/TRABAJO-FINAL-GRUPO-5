    private void agregaEntrada(){
       double precio,total,importe; 
       modeloE = (DefaultTableModel) TablaS.getModel();
       int idEntrada=Integer.parseInt(txtIDEntradaS.getText());
       int idSalida=Integer.parseInt(txtnumSa.getText());
       int cantidad=Integer.parseInt(txtCantidadS.getText());
       precio = Double.parseDouble(txtPrecioS.getText());
       String prod = txtProductoS.getText();
       importe=cantidad*precio;
       int stock = Integer.parseInt(txtStockS.getText());
     ArrayList lista = new ArrayList();
if (stock > 0 && cantidad <= stock) {
    lista.add(idSalida);
    lista.add(idEntrada);
    lista.add(prod);
    lista.add(precio);
    lista.add(cantidad);
    lista.add(importe);

    Object[] ob = new Object[4];
    ob[0] = lista.get(0);
    ob[1] = lista.get(1);
    ob[2] = lista.get(2);
    ob[3] = lista.get(3);
    ob[4] = lista.get(4);
    ob[5] = lista.get(5);
    modeloE.addRow(ob);

    TablaS.setModel(modeloE);
} else {
    MenuPrincipal m = new MenuPrincipal();
    m.warning("Stock Insuficiente");
}
    } 

    void GuardarDetalle(){
        for(int i = 0; i<TablaS.getRowCount();i++){
        int idSalida = Integer.parseInt(TablaS.getValueAt(i, 0).toString());
        int idEntrada = Integer.parseInt(TablaS.getValueAt(i, 1).toString());
        int cant = Integer.parseInt(TablaS.getValueAt(i, 4).toString());  
        double importe = Double.parseDouble(TablaS.getValueAt(i, 5).toString());
        cdeta.insertar(idSalida, idEntrada, cant, importe);
        }
    }
   
    void sumarTotal(){
        double fila, igv, subtotal, total = 0;
        for (int i = 0; i < TablaS.getRowCount(); i++) {
        fila = Double.parseDouble(TablaS.getValueAt(i, 5).toString());
        total += fila;
        igv = total * 0.18;
        subtotal = total - igv;
        
        txtTotal.setText(String.format("4.2f", total).replace(",", "."));
        txtSubtotal.setText(String.format("4.2f", subtotal).replace(",", "."));
        txtIGV.setText(String.format("4.2f", igv).replace(",", "."));
        }  
    }
    
