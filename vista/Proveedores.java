  private void listarProveedores() {
 List<proveedor> listaProveedores = ctp.listarProveedor();
modeloPro = (DefaultTableModel) tablaProv.getModel();
Object[] ob = new Object[8];  // Arreglo de 7 elementos

for (int i = 0; i < listaProveedores.size(); i++) {
    // Obtener los datos de cada cliente de la lista
    ob[0] = listaProveedores.get(i).getIdProveedor();
    ob[1] = listaProveedores.get(i).getNombre();
    ob[2] = listaProveedores.get(i).getApellido();
    ob[3] = listaProveedores.get(i).getDocumento();
    ob[4] = listaProveedores.get(i).getRazonS();
    ob[5] = listaProveedores.get(i).getDireccion();
    ob[6] = listaProveedores.get(i).getTelefono();
    ob[7] = listaProveedores.get(i).getCorreo();

    // AÃ±adir la fila con los datos del cliente
    modeloPro.addRow(ob);
   }
}
