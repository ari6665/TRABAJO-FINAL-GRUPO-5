    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
           
        int file=tablaCLientes.getSelectedRow();
         if (file ==-1){
             JOptionPane.showMessageDialog(null,"seleccione un Cliente");
         }else if (txtIdCli.getText().isEmpty()) {
              JOptionPane.showMessageDialog(null, "Seleccione una categoría");
             
         }else{
             c.setIdCliente(Integer.parseInt(txtIdCli.getText()));
             c.setNombre(txtNombreCli.getText());
             c.setApellido(txtApellidoCli.getText());
             c.setDocumento(txtDocumentoCli.getText());
             c.setDireccion(txtDireccionCli.getText());
             c.setTelefono(txtTelefonoCli.getText());
             c.setCorreo(txtCorreoCli.getText());
             
             if(ctcl.editar(c)){
             //JOptionPane.showMessageDialog(null,"se modifico con exito");
              MenuPrincipal me =new MenuPrincipal(); 
              me.exito("modificado con exito");
             limpiarTablaClientes();
             listarClientes();
             limpiarCampos();
             }
         }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
         // TODO add your handling code here:
        if (!txtIdCli.getText().isEmpty()) {
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿De verdad Estas seguro de eliminar el cliente?", "Confirmar", 2);
        if (confirmacion == 0) {
            c.setIdCliente(Integer.parseInt(txtIdCli.getText()));
            ctcl.eliminar(c);
            limpiarTablaClientes();
            listarClientes();
            limpiarCampos();
            //JOptionPane.showMessageDialog(null, "Se elimino con exito el cliente");
             MenuPrincipal me =new MenuPrincipal(); 
             me.warning("Se elimino con exito");
         }
      }else{
            JOptionPane.showMessageDialog(null, "Seleccione un cliente");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


