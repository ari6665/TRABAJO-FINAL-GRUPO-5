     private void listarClientes() {
 List<Clientes> listaClientes = ctcl.listarClientes();
modeloCliente = (DefaultTableModel) tablaCLientes.getModel();
Object[] ob = new Object[7];  // Arreglo de 7 elementos

for (int i = 0; i < listaClientes.size(); i++) {
    // Obtener los datos de cada cliente de la lista
    ob[0] = listaClientes.get(i).getIdCliente();
    ob[1] = listaClientes.get(i).getNombre();
    ob[2] = listaClientes.get(i).getApellido();
    ob[3] = listaClientes.get(i).getDocumento();
    ob[4] = listaClientes.get(i).getDireccion();
    ob[5] = listaClientes.get(i).getTelefono();
    ob[6] = listaClientes.get(i).getCorreo();

    // Añadir la fila con los datos del cliente
    modeloCliente.addRow(ob);
   }
}
