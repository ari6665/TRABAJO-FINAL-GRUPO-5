    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
            c.setDocumento(txtDocumentoCli.getText());
              if (ctcl.buscar(c)) {
                txtIdCli.setText(c.getIdCliente()+"");
                txtNombreCli.setText(c.getNombre());
                txtApellidoCli.setText(c.getApellido());
                txtDocumentoCli.setText(c.getDocumento());
                txtDireccionCli.setText(c.getDireccion());
                txtTelefonoCli.setText(c.getTelefono());
                txtCorreoCli.setText(c.getCorreo());
            } else {
              JOptionPane.showMessageDialog(null, "El cliente no ha sido encontrado");
              limpiarCampos();
             }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
  
       // TODO add your handling code here:
       c.setNombre(txtNombreCli.getText());
       c.setApellido(txtApellidoCli.getText());
       c.setDocumento(txtDocumentoCli.getText());
       c.setDireccion(txtDireccionCli.getText());
       c.setTelefono(txtTelefonoCli.getText());
       c.setCorreo(txtCorreoCli.getText());
       if(ctcl.insertar(c)){
          // JOptionPane.showMessageDialog(null, "Registro de categoría completado");
           MenuPrincipal me =new MenuPrincipal(); 
           me.exito("cliente registrado con exito");
           limpiarCampos();
           limpiarTablaClientes();
           listarClientes();
           
       }else{
           MenuPrincipal me =new MenuPrincipal(); 
           me.warning("no se  pudo registrar al cliente ");
       }             
    }//GEN-LAST:event_btnGuardarActionPerformed

